    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        admin_code code=new admin_code();
        admin_props p= new admin_props();
        if(jTextField1.isEnabled())
        p.setmonthly(Float.parseFloat(jTextField1.getText()));
        else
        {
          p.setmonthly(0);
        }
        if(jTextField2.isEnabled())
        p.setquarter(Float.parseFloat(jTextField2.getText()));
        else
        {
          p.setquarter(0);
        }
        if(jTextField3.isEnabled())
        p.sethalfyr(Float.parseFloat(jTextField3.getText()));
        else
        {
          p.sethalfyr(0);
        }
        if(code.updatecharges())
        {
            JOptionPane.showMessageDialog(rootPane, "charges updated successfully");
            reset();
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, code.get_err());
        }
    }//GEN-LAST:event_jButton4ActionPerformed
