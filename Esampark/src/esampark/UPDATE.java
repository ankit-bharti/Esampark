    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        admin_props p=new admin_props();
        if(jComboBox1.getSelectedItem().toString().equalsIgnoreCase("senior citizen"))
        {
            p.setcategory("senior");
        }
        else
        {
        p.setcategory(jComboBox1.getSelectedItem().toString());
        }
        admin_code code= new admin_code();
        if(code.getcharges())
        {
            int count= jPanel1.getComponentCount();
        Component c[]=jPanel1.getComponents();
        for(int i=0;i<count;i++)
        {
            c[i].setEnabled(true);
        }
        jTextField1.setEnabled(false);
        jTextField2.setEnabled(false);
        jTextField3.setEnabled(false);
            jLabel5.setText(String.valueOf(p.getmonthly()));
            jLabel6.setText(String.valueOf(p.getquarter()));
            jLabel9.setText(String.valueOf(p.gethalfyr()));
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, code.get_err());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

